# Crie um script que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor

n = int(input('Digite um numero inteiro: '))
print(f'O numero que você escolheu foi {n} e, de acordo com isso:\nSeu sucessor é {n+1}\nE seu antecessor é {n-1}')