# Faça um algoritmo que leia o salário de um funcionário e mostre seu novo salário com a 15% de almento

salario = float(input('Digite seu salário atual: R$ '))
print(f'Parabéns! você ganhou 15% de aumento e agora seu novo salário é de R$ {salario+(salario*0.15)}')