# Crie um algorítimo que leia um número e mostre seu dobro, triplo e raiz quadrada

n = int(input('Digite um número: '))
d = n*2
t = n*3
rq = n**(1/2)
print(f'Você escolheu o número: {n}.\nSeu dobro é {d}\nSeu triplo é {t}\nE sua raiz quadrada é {rq}')